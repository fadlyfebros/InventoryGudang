    private void connectToDB() {
        try {
            conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Koneksi ke database gagal: " + e.getMessage());
        }
    }
    private void loadData() {
        DefaultTableModel model = new DefaultTableModel(new String[]{
                "Kode Barang", "Nama Barang", "Berat", "Kode Supplier", "Nama Supplier",
                "Stok Awal", "Harga Beli", "Harga Jual", "Satuan"}, 0);

        try (Connection c = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
             Statement s = c.createStatement();
             ResultSet r = s.executeQuery("SELECT * FROM tb_barang")) {

            while (r.next()) {
                String kodeBarang = r.getString("kodeBarang");
                String namaBarang = r.getString("namaBarang");
                String berat = r.getString("berat");
                String kodeSupplier = r.getString("kodeSupplier");
                String namaSupplier = r.getString("namaSupplier");
                int stokAwal = r.getInt("StokAwal");
                double hargaBeli = r.getDouble("hargaBeli");
                double hargaJual = r.getDouble("hargaJual");
                String satuan = r.getString("satuan");

                model.addRow(new Object[]{
                        kodeBarang, namaBarang, berat, kodeSupplier, namaSupplier,
                        stokAwal, hargaBeli, hargaJual, satuan});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        tblReport.setModel(model);
    }

    // Helper method to get the connection object
