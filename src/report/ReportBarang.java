    private void btnPencarianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPencarianActionPerformed
        String keyword = txtPencarianBarang.getText().trim();

        // Query SQL untuk mencari barang berdasarkan nama
        String sql = "SELECT * FROM tb_barang WHERE namaBarang LIKE ?";

        try {
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, "%" + keyword + "%");

            ResultSet rs = stmt.executeQuery();

            // Menampilkan hasil ke tabel tblReport
            DefaultTableModel model = new DefaultTableModel(new String[]{
                    "Kode Barang", "Nama Barang", "Berat", "Kode Supplier", "Nama Supplier",
                    "Stok Awal", "Harga Beli", "Harga Jual", "Satuan"}, 0);

            while (rs.next()) {
                String kodeBarang = rs.getString("kodeBarang");
                String namaBarang = rs.getString("namaBarang");
                String berat = rs.getString("berat");
                String kodeSupplier = rs.getString("kodeSupplier");
                String namaSupplier = rs.getString("namaSupplier");
                int stokAwal = rs.getInt("StokAwal");
                double hargaBeli = rs.getDouble("hargaBeli");
                double hargaJual = rs.getDouble("hargaJual");
                String satuan = rs.getString("satuan");

                model.addRow(new Object[]{
                        kodeBarang, namaBarang, berat, kodeSupplier, namaSupplier,
                        stokAwal, hargaBeli, hargaJual, satuan});
            }

            tblReport.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnPencarianActionPerformed

